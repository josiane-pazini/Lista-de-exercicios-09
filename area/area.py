# Crie uma função que calcule a área de um quadrado, de um retângulo e de um triângulo

#l = lado, b = base, h = altura

def area_quadrado (l):
    resultado = l * l
    return resultado 

def area_retangulo (b, h):             
    return (b*h)

def area_triangulo (b, h):
    return (b*h)/2

def area_retangulo_lista (b, h):             
    return (b*h)